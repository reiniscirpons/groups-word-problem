(** In this section we develop the Nielsen-Schreier-Stallings
    theory of subgroups of the free group, as it applies
    to the subgroup generated by a_p = b^p a b^{-p} and b^q. *)
Section NielsenSchreierStallings.

Let Sigma := ordinal 2.
Let F2P := FGP Sigma.
Let F2 := FreeGroup Sigma.
Notation a := (Base 0).
Notation b := (Base 1).
Notation "a^-1" := (Inverse 0).
Notation "b^-1" := (Inverse 1).

(* NOTE(reiniscirpons): a_p = b^p a b^{-p} *)
Definition a_encoding (p: int): F2 :=
  (power (`[b]_F2P) p) @ (`[a]_F2P) @ inv (power (`[b]_F2P) p).

(* NOTE(reiniscirpons): b^q is the other generator *)
Definition b_encoding (q: int): F2 := power (`[b]_F2P) q.

Variable p q: nat.

Let gens := [:: a_encoding p; b_encoding q].
(* TODO: Why is Gamma not equal to Sigma? Shouldn't they be because
         length gens computationally reduces to 2? *)
Let Gamma := ordinal (size gens).
Let IndexFG := FreeGroup Gamma.
Let K := generatedSubgroup gens.

(* NOTE(reiniscirpons): This is the map a -> a_p and b -> b^q,
   we will show it is an isomorphism onto <a_p, b^q> later on. *)
(* TODO(reiniscirpons): Why does this not work without explicitly
        specifying the group and subgroup? *)
Definition apbq_projection: morphism IndexFG F2 := 
  (@subgroup_inj F2 K) \o (subgroup_projection gens).

(* TODO(reiniscirpons): We need to probably get rid of prod or change
   its definition to be recursive on the input list instead of 
   having the iterated definition that is too hard to reason about
   (when we eliminate it causes a lot of headache) *)
Lemma apbq_projection_cons: forall h t,
  apbq_projection (h::t) =
    (match h with
    | Base o => if (o == 0)%B then a_encoding p else b_encoding q
    | Inverse o => inv (if (o == 0)%B then a_encoding p else b_encoding q)
    end) ++ apbq_projection t.
Proof. by move => [|] [] [|[|//]]. Qed.

Variable Hpq: (p*2 < q)%N.

Lemma fra_encode: forall n, n!=0 -> freely_reduced (a_encoding n).
Proof.
  move => n Hn;
  rewrite /a_encoding /inv /= -power_inv_word -[power _ (- n)]cats0.
  set x := [::a] \mod (FGP Sigma).
  rewrite -[x]FreeGroup_power1 /x
  [power]lock -!cat_law -catA !cat_law -lock.
  apply (freely_reduced_power _
          ([::b; a; b] \mod (FGP Sigma))
          [:: n; 1; -n]) => [//||//|//].
  apply /allP => m.
  rewrite !in_cons => /orP [|/orP [|/orP [|//]]]; by lia.
Qed.

Lemma apbq_minimal: @minimality_condition _ gens.
Proof.
  have Hred: (forall w, freely_reduced w -> FreeGroup_norm w = w);
    first by move => T w /freely_reduced_correct.
  move => [|[|[|[|//]]]] [|[|[|[|//]]]] _ _ // _ /=;
  rewrite /a_encoding /b_encoding /= =>
    /Order.PreorderTheory.ltW /CmpOrder.cmp_sz_le /=;
  rewrite /CmpOrder.sz /=;
  case Hp: p Hpq => [//|p'] Hpq';
  case Hq: q Hpq' => [//|q'] Hpq'.
  (* Make tactic to take care of this *)
  rewrite -Hp !associativity -5!associativity 
          [(inv _) @ _]associativity inverse_right
          neutral_left -(FreeGroup_power1 _ ([::a]: F2))
          -power_inv [power]lock !associativity -lock
          -[(_ @ _) @ power _ 1]associativity
          -power_add -[power _ (- _)]cats0
          [power]lock
          -!associativity -lock.
  rewrite !Hred.
  - rewrite !size_cat !(@size_power F2P) /=; lia.
  - apply (freely_reduced_power _
          ([::b; a; b] \mod (FGP Sigma))
          [:: (p: int); 1; -(p: int)]) => [//||//|//].
    apply/allP => m.
    rewrite !in_cons => /orP [|/orP [|/orP [|//]]]; by lia.
  - apply (freely_reduced_power _
          ([::b; a; b] \mod (FGP Sigma))
          [:: (p: int); 2; -(p: int)]) => [//||//|//].
    apply/allP => m.
    rewrite !in_cons => /orP [|/orP [|/orP [|//]]]; by lia.
  (* Only 21 cases to go! *)
Admitted.


Lemma frgens: forall x, x \in gens -> freely_reduced x.
Proof.
  move => x.
  rewrite /gens in_cons => /orP [/eqP -> |].
  case: (p =P 0) => [-> |/eqP].
  + rewrite /a_encoding power0 -!cat_law cats0 cat0s.
    by rewrite freely_reduced_correct.
  
  + move => Hlt0.
    by apply: fra_encode.
  - rewrite in_cons => /orP [/eqP -> |//].
    apply: freely_reduced_power1.
Qed.
  
Lemma non_trivial: forall x, x \in gens -> FreeGroup_norm x != e.
Proof.
  move => x ingens.
  move: (frgens x ingens).
  rewrite /gens in_cons in ingens; move: ingens => /orP [/eqP -> |].
  rewrite freely_reduced_correct => <-; rewrite /a_encoding.
  apply/eqP => Habs.
  by move/(f_equal size) in Habs; rewrite -!cat_law !size_cat /= in Habs; lia.
  rewrite in_cons freely_reduced_correct => /orP [/eqP -> |//] <-; rewrite /b_encoding.
  apply/eqP => Habs.
  move/(f_equal size) in Habs; rewrite /= (@size_power F2P) in Habs.
  by lia.
Qed.


Definition apbq_isomorphism: isomorphism F2 K.
Proof.
  move: (@subfree_group_iso Sigma gens).
  move: (@NielsenR_fixpoint Sigma gens apbq_minimal frgens non_trivial) => -> /=.
  by done.
Qed.

Definition apbq_isomorphism_inv': K -> F2.
Proof.
  move => x.
  move: (@surjectivity_property _ _ apbq_isomorphism x) => H.

  rewrite /K /generatedSubgroup in x H.

  have H_clean : exists x0 : F2, (fun y => (FreeGroup_norm((apbq_isomorphism y).(sb_point)) == FreeGroup_norm (x.(sb_point)))%B) x0.
    case: H => x0 Heq.
    exists (x0).
    rewrite /eq /= /subgroupby_eq /subgroupby_inj in Heq.
    by rewrite Heq.
  by apply: (@xchoose F2 _ H_clean).
Qed.

  
Definition apbq_isomorphism_inv: isomorphism K F2.
Proof.
Admitted.
  

End NielsenSchreierStallings.

