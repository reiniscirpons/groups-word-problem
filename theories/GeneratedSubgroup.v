Lemma F2_powerS':
  forall (w: F2) (n: nat),
    power w n.+1 = power w n @ w.
Proof.
  move => w; elim/nat_pairs_ind => [||n IH1 IH2].
  - by rewrite power0 /power /= -!cat_law cat0s cats0.
  - by rewrite /power /= -!cat_law !cats0.
  - by rewrite powerS {1}IH2 [power w n.+2]powerS -!cat_law catA.
Qed.


